#include "libft.h"

char	**ft_split(char const *s, char c)
{
	char **str;
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			j++;
		i++;
	}
}
